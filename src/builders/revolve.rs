#[derive(Debug, Error)]
pub enum RevolveError {
    #[error("The provided profile is not planar")]
    PlanarError(PlanarityError),

    #[error("Missing vertex point for dart {dart:?}")]
    MissingVertexPoint { dart: Dart },

    #[error("Missing edge curve for dart {dart:?}")]
    MissingEdgeCurve { dart: Dart },

    #[error("Missing face for key {key:?}")]
    MissingFace { key: FaceKey },

    #[error("Only line edges can be partially revolved for now: {dart:?}")]
    UnsupportedPartialRevolveCurve { dart: Dart },

    #[error("Darts {first:?} and {second:?} are not sewable in dimension {dim:?}")]
    SewFailed { dim: Dim, first: Dart, second: Dart },
}

/// Adds the circular sweep topology generated by revolving one edge around `axis`.
///
/// Full revolutions are handled as a special case: the two source vertices each
/// get a closed circular edge, with no duplicate endpoint vertices. Partial
/// revolutions create a swept quad using the source edge as the first boundary,
/// two circular side edges, and a rotated copy of the source edge.
pub fn add_revolved_edge<P: Payload>(
    g: &mut GMap<P>,
    edge_dart: Dart,
    axis: Axis3,
    angle: Rad64,
) -> Result<Dart, RevolveError> {
    let angle = angle.clamp(Angle::ZERO, Angle::FULL_TURN);
    if is_full_turn(angle) {
        return add_full_revolved_edge(g, edge_dart, axis);
    }

    add_partial_revolved_edge(g, edge_dart, axis, angle)
}

fn add_full_revolved_edge<P: Payload>(
    g: &mut GMap<P>,
    edge_dart: Dart,
    axis: Axis3,
) -> Result<Dart, RevolveError> {
    let vertices = edge_vertices(g, edge_dart)?;
    let mut circles = vertices
        .into_iter()
        .map(|(dart, point)| add_closed_revolve_circle(g, dart, axis, point));
    let first = circles
        .next()
        .expect("an edge always has at least one incident vertex")?;

    for circle in circles {
        circle?;
    }

    Ok(first)
}

fn add_partial_revolved_edge<P: Payload>(
    g: &mut GMap<P>,
    edge_dart: Dart,
    axis: Axis3,
    angle: Rad64,
) -> Result<Dart, RevolveError> {
    let (start, end, curve, end_dart) = edge_geometry(g, edge_dart)?;
    let rotated_start = rotate_point(axis, start, angle);
    let rotated_end = rotate_point(axis, end, angle);
    let rotated_curve = rotate_curve(edge_dart, &curve, axis, angle)?;

    let start_arc = add_open_revolve_circle(g, axis, start, rotated_start)?;
    let end_arc = if same_vertex(start, end) {
        start_arc
    } else {
        add_open_revolve_circle(g, axis, end, rotated_end)?
    };
    let (rotated_edge, _) = add_edge(g, rotated_start, rotated_end, rotated_curve);
    let rotated_edge_end = g.alpha(Dim::Zero, rotated_edge);
    let start_arc_end = g.alpha(Dim::Zero, start_arc);
    let end_arc_end = g.alpha(Dim::Zero, end_arc);

    if end_arc != start_arc {
        sew(g, Dim::One, end_dart, end_arc)?;
        sew(g, Dim::One, end_arc_end, rotated_edge_end)?;
    }
    sew(g, Dim::One, rotated_edge, start_arc_end)?;
    sew(g, Dim::One, start_arc, edge_dart)?;

    Ok(rotated_edge)
}

fn add_open_revolve_circle<P: Payload>(
    g: &mut GMap<P>,
    axis: Axis3,
    start: Point3,
    end: Point3,
) -> Result<Dart, RevolveError> {
    Ok(add_edge(g, start, end, revolve_circle_curve(axis, start)).0)
}

fn add_closed_revolve_circle<P: Payload>(
    g: &mut GMap<P>,
    _source_vertex_dart: Dart,
    axis: Axis3,
    point: Point3,
) -> Result<Dart, RevolveError> {
    let projected = axis.project(point);
    let radius = distance(&projected, &point);
    let first = g.add_dart();
    let second = g.add_dart();
    g.sew_unchecked(Dim::Zero, first, second);
    g.sew_unchecked(Dim::One, first, second);
    g.add_vertex(VertexAttr::new(first, point, P::V::default()));
    g.add_edge(EdgeAttr::new(
        first,
        Curve::Circle(Circle::from_axis(
            Axis3::new(projected, axis.direction),
            radius,
        )),
        P::E::default(),
    ));
    Ok(first)
}

fn edge_geometry<P: Payload>(
    g: &GMap<P>,
    edge_dart: Dart,
) -> Result<(Point3, Point3, Curve, Dart), RevolveError> {
    let edge = Edge::new(g, edge_dart);
    let start = *edge
        .start()
        .point()
        .ok_or(RevolveError::MissingVertexPoint { dart: edge_dart })?;
    let end_dart = g.alpha(Dim::Zero, edge_dart);
    let end = *edge
        .end()
        .point()
        .ok_or(RevolveError::MissingVertexPoint { dart: end_dart })?;
    let curve = edge
        .curve()
        .ok_or(RevolveError::MissingEdgeCurve { dart: edge_dart })?;
    Ok((start, end, curve.clone(), end_dart))
}

fn edge_vertices<P: Payload>(
    g: &GMap<P>,
    edge_dart: Dart,
) -> Result<Vec<(Dart, Point3)>, RevolveError> {
    let edge = Edge::new(g, edge_dart);
    edge.vertices()
        .into_iter()
        .map(|vertex| {
            let point = *vertex
                .point()
                .ok_or(RevolveError::MissingVertexPoint { dart: vertex.dart })?;
            Ok((vertex.dart, point))
        })
        .collect()
}

fn rotate_curve(
    dart: Dart,
    curve: &Curve,
    axis: Axis3,
    angle: Rad64,
) -> Result<Curve, RevolveError> {
    match curve {
        Curve::Line(_) => Ok(Curve::Line(Line::new(
            rotate_point(axis, curve.point_at(0.0), angle),
            rotate_point(axis, curve.point_at(1.0), angle),
        ))),
        Curve::Circle(_) | Curve::Nurbs(_) => {
            Err(RevolveError::UnsupportedPartialRevolveCurve { dart })
        }
    }
}

fn rotate_point(axis: Axis3, point: Point3, angle: Rad64) -> Point3 {
    axis.origin + Rotation3::from_axis_angle(&axis.direction, angle.val()) * (point - axis.origin)
}

fn is_full_turn(angle: Rad64) -> bool {
    (angle.val() - Rad64::FULL_TURN.val()).abs() <= ANGULAR_TOLERANCE
}

fn same_vertex(a: Point3, b: Point3) -> bool {
    (a - b).norm_squared() <= LINEAR_TOLERANCE * LINEAR_TOLERANCE
}

fn sew<P: Payload>(
    g: &mut GMap<P>,
    dim: Dim,
    first: Dart,
    second: Dart,
) -> Result<(), RevolveError> {
    g.sew(dim, first, second)
        .map_err(|_| RevolveError::SewFailed { dim, first, second })
}

pub fn add_revolved_profile<P: Payload>(
    g: &mut GMap<P>,
    profile_dart: Dart,
    axis: Axis3,
    angle: Rad64,
) -> Result<Dart, RevolveError> {
    let profile = Profile::new(g, profile_dart);
    let planar = Planar::new(profile).map_err(RevolveError::PlanarError)?;
    let profile = planar.inner();
    if profile.is_closed() {
        return Ok(add_revolved_profile_faces(g, profile_dart, axis, angle, true)?.swept_dart);
    }

    Ok(add_revolved_profile_faces(g, profile_dart, axis, angle, false)?.swept_dart)
}

struct RevolvedProfile {
    swept_dart: Dart,
}

struct RevolvedFace {
    start_side: Dart,
    end_side: Dart,
    outer_loop: Dart,
}

fn add_revolved_profile_faces<P: Payload>(
    g: &mut GMap<P>,
    profile_dart: Dart,
    axis: Axis3,
    angle: Rad64,
    close_ring: bool,
) -> Result<RevolvedProfile, RevolveError> {
    let angle = angle.clamp(Angle::ZERO, Angle::FULL_TURN);
    let profile = Profile::new(g, profile_dart);
    let edge_darts = profile
        .edges()
        .into_iter()
        .map(|edge| edge.dart)
        .collect::<Vec<_>>();

    let mut faces = Vec::with_capacity(edge_darts.len());
    for edge_dart in edge_darts {
        faces.push(add_revolved_edge_face(g, edge_dart, axis, angle)?);
    }

    sew_revolved_faces(g, &faces, close_ring)?;
    let swept_dart = faces.first().map_or(profile_dart, |face| face.outer_loop);
    Ok(RevolvedProfile { swept_dart })
}

fn add_revolved_edge_face<P: Payload>(
    g: &mut GMap<P>,
    edge_dart: Dart,
    axis: Axis3,
    angle: Rad64,
) -> Result<RevolvedFace, RevolveError> {
    let (start, end, curve, _) = edge_geometry(g, edge_dart)?;
    let rotated_start = rotate_point(axis, start, angle);
    let rotated_end = rotate_point(axis, end, angle);
    let rotated_curve = rotate_curve(edge_dart, &curve, axis, angle)?;
    let start_arc = revolve_circle_curve(axis, start);
    let end_arc = revolve_circle_curve(axis, end);
    let (u0, u1) = curve.parameters_between(start, end);
    let surface = Surface::Revolution(SurfaceOfRevolution::new(
        curve.clone(),
        axis.origin,
        axis.direction,
    ));
    let pcurves = [
        (Point2::new(u0, 0.0), Point2::new(u1, 0.0)),
        (Point2::new(u1, 0.0), Point2::new(u1, angle.val())),
        (Point2::new(u1, angle.val()), Point2::new(u0, angle.val())),
        (Point2::new(u0, angle.val()), Point2::new(u0, 0.0)),
    ];

    add_revolved_quad_face(
        g,
        [start, end, rotated_end, rotated_start],
        [curve, end_arc, rotated_curve, start_arc],
        surface,
        pcurves,
    )
}

fn add_revolved_quad_face<P: Payload>(
    g: &mut GMap<P>,
    corners: [Point3; 4],
    boundary_curves: [Curve; 4],
    surface: Surface,
    pcurves: [(Point2, Point2); 4],
) -> Result<RevolvedFace, RevolveError> {
    let darts: Vec<Dart> = (0..8).map(|_| g.add_dart()).collect();

    for i in 0..4 {
        sew(g, Dim::Zero, darts[2 * i], darts[2 * i + 1])?;
    }
    for i in 0..4 {
        sew(
            g,
            Dim::One,
            darts[2 * i + 1],
            darts[(2 * i + 2) % darts.len()],
        )?;
    }

    for i in 0..4 {
        let dart = g.cell_representative(darts[2 * i], Dim::Zero);
        g.add_vertex(VertexAttr::new(dart, corners[i], P::V::default()));
    }

    for i in 0..4 {
        let edge_dart = g.cell_representative(darts[2 * i], Dim::One);
        g.add_edge(EdgeAttr::new(
            edge_dart,
            boundary_curves[i].clone(),
            P::E::default(),
        ));
    }

    g.add_face(FaceAttr::with_pcurves(
        surface,
        P::F::default(),
        darts[0],
        Vec::new(),
        quad_pcurves(&pcurves, &darts),
    ));

    Ok(RevolvedFace {
        start_side: darts[7],
        end_side: darts[2],
        outer_loop: darts[0],
    })
}

fn sew_revolved_faces<P: Payload>(
    g: &mut GMap<P>,
    faces: &[RevolvedFace],
    close_ring: bool,
) -> Result<(), RevolveError> {
    for i in 0..faces.len().saturating_sub(1) {
        sew(g, Dim::Two, faces[i].end_side, faces[i + 1].start_side)?;
    }

    if close_ring && !faces.is_empty() {
        sew(
            g,
            Dim::Two,
            faces[faces.len() - 1].end_side,
            faces[0].start_side,
        )?;
    }

    Ok(())
}

fn revolve_circle_curve(axis: Axis3, point: Point3) -> Curve {
    let projected = axis.project(point);
    let radius = distance(&projected, &point);
    if radius <= LINEAR_TOLERANCE {
        return Curve::Circle(Circle::from_axis(
            Axis3::new(projected, axis.direction),
            radius,
        ));
    }

    Curve::Circle(Circle::new(
        Plane::new(projected, point - projected, axis.direction),
        radius,
    ))
}

fn quad_pcurves(uv: &[(Point2, Point2); 4], darts: &[Dart]) -> HashMap<Dart, Curve2> {
    let mut pcurves = HashMap::with_capacity(4);
    for i in 0..4 {
        pcurves.insert(darts[2 * i], Curve2::Line(Line2::new(uv[i].0, uv[i].1)));
    }
    pcurves
}

pub fn add_revolved_face<P: Payload>(
    g: &mut GMap<P>,
    face_key: FaceKey,
    axis: Axis3,
    angle: Rad64,
) -> Result<SolidKey, RevolveError> {
    let face = g
        .face(face_key)
        .map(|attr| attr.face(g))
        .ok_or(RevolveError::MissingFace { key: face_key })?;
    let mut loops = Vec::with_capacity(1 + face.inner_loops().len());
    loops.push(face.outer_loop().dart);
    loops.extend(face.inner_loops().into_iter().map(|loop_| loop_.dart));

    let mut shell = None;
    for loop_dart in loops {
        let revolved = add_revolved_profile(g, loop_dart, axis, angle)?;
        shell.get_or_insert(revolved);
    }

    let shell = shell.expect("a face should have at least one boundary loop");
    Ok(g.add_solid(SolidAttr::new(P::S::default(), shell, None)))
}

pub fn face_key_for_dart<P: Payload>(g: &GMap<P>, dart: Dart) -> Option<FaceKey> {
    g.attribute::<Cell2>(dart).copied()
}
